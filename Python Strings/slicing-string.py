# characters from position 3 to position 5 
b = "Hello, World!"
print(b[3:5])

# characters from the start to position 5
b = "Hello, World!"
print(b[:5])

# characters from position 2, and all the way to the end
b = "Hello, World!"
print(b[2:])

# "o" in "World!" (position -5) To, but not included: "d" in "World!" (position -2)
b = "Hello, World!"
print(b[-5:-2])